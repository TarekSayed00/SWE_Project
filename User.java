
public class User {
	private String UserName;
	private String Password;
	
	public void setUserName (String name)
	{
		this.UserName=name;
	}
	
	public String getUserName ()
	{
		return UserName;
	}
	
	public void setPassword(String pass)
	{
		this.Password=pass;
	}
	public String getPassword()
	{
		return Password;
	}
	

}
